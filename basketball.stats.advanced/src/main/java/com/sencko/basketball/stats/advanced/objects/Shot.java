package com.sencko.basketball.stats.advanced.objects;

/**
 *
 * @author i028512
 */
public class Shot {

  private Integer y;
  private Integer p;
  private Integer pts;
  private Integer r;
  private Integer per;
  private String title;
  private Integer x;

  public Integer getY() {
    return y;
  }

  public void setY(Integer y) {
    this.y = y;
  }

  public Integer getP() {
    return p;
  }

  public void setP(Integer p) {
    this.p = p;
  }

  public Integer getPts() {
    return pts;
  }

  public void setPts(Integer pts) {
    this.pts = pts;
  }

  public Integer getR() {
    return r;
  }

  public void setR(Integer r) {
    this.r = r;
  }

  public Integer getPer() {
    return per;
  }

  public void setPer(Integer per) {
    this.per = per;
  }

  public String getTitle() {
    return title;
  }

  public void setTitle(String title) {
    this.title = title;
  }

  public Integer getX() {
    return x;
  }

  public void setX(Integer x) {
    this.x = x;
  }

  public Shot() {
  }

}
